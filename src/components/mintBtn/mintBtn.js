const MintBtn = () => {
    const [status, setStatus] = useState("");
    const [provider, setProvider] = useState(null);
    const signerAddress = useSelector(selectUserAddress)

    useEffect(() => {
      if (window.ethereum == null) {
        setStatus("MetaMask not installed; using read-only defaults");
        setProvider(ethers.getDefaultProvider())
      } else
        setProvider(new ethers.BrowserProvider(window.ethereum))
    }, []);

    const handleMint = async () => {
      if (!provider) {
        console.log('Provider not found');
        return;
      }
  
      const signer = await provider.getSigner();
      const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, signer);
  
      try {
        const mintAmount = 1; // Adjust this value for the desired mint amount
        const tx = await contract.mint(mintAmount, { value: ethers.parseEther('0.1') }); // Adjust the value according to your NFT's cost
        console.log('Transaction sent:', tx.hash);
        await tx.wait();
        console.log('Transaction confirmed:', tx.hash);
      } catch (error) {
        console.error('Error minting:', error);
      }
    };

    return (
        <div>
            {/* <button onClick={mintNFT}>Mint NFT 11</button> */}
            <button onClick={handleMint}>Mint NFT</button>
            {status && <p style={{
                color:  "black"
            }}>{status}</p>}
        </div>
    );
};

export default MintBtn;